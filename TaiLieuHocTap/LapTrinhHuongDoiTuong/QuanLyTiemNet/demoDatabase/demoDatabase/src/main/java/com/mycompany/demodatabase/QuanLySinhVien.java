    private void tblSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSinhVienMouseClicked
        int row = tblSinhVien.getSelectedRow();
        if (row >= 0) {
            txtMaSV.setText(tblSinhVien.getValueAt(row, 0).toString());
            txtHolot.setText(tblSinhVien.getValueAt(row, 1).toString());
            txtTen.setText(tblSinhVien.getValueAt(row, 2).toString());
            txtMaLop.setText(tblSinhVien.getValueAt(row, 3).toString());
            txtTenLop.setText(tblSinhVien.getValueAt(row, 4).toString());
            txtSoDT.setText(tblSinhVien.getValueAt(row, 5).toString());
            txtEmail.setText(tblSinhVien.getValueAt(row, 6).toString());
            
            btnCapNhat.setEnabled(true);
            btnXoa.setEnabled(true);

        }
    }//GEN-LAST:event_tblSinhVienMouseClicked

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
         try {
            
            SinhVien sv = new SinhVien();
            sv.setMaSV(txtMaSV.getText());
            sv.setHolot(txtHolot.getText());
            sv.setTen(txtTen.getText());
            sv.setMalop(txtMaLop.getText());
            sv.setTenlop(txtTenLop.getText());
            sv.setSoDT(txtSoDT.getText());
            sv.setEmail(txtEmail.getText());
            
            SinhvienDAO dao = new SinhvienDAO();
            dao.insert(sv);
            
            JOptionPane.showMessageDialog(this, "Thêm thành công 1 sinh viên");
            fillTable();
            resetForm();
            btnLuu.setEnabled(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLuuActionPerformed

