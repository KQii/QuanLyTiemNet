    private void initTable() {
        String[] header = new String[]{"Mã SV", "Họ lót", "Tên", "Mã lớp", "Tên Lớp", "Số ĐT", "Email"};
        tblModel.setColumnIdentifiers(header);
        tblSinhVien.setModel(tblModel);
    }

    private void fillTable() {
        List<SinhVien> lstStudent = new ArrayList<>();
        try {
            SinhvienDAO dao = new SinhvienDAO();

            lstStudent = dao.getAll();
        } catch (Exception e) {
            e.printStackTrace();
        }

        tblModel.setRowCount(0);
        for (SinhVien student : lstStudent) {
            tblModel.addRow(new String[]{student.getMaSV(), student.getHolot(), student.getTen(), student.getMalop(), student.getTenlop(), student.getSoDT(), student.getEmail()});
        }
        tblModel.fireTableDataChanged();
    }

